//  Init / query
bool fb_init(const FBInfo& info) {
    if (!info.addr || info.bpp != 32) return false;
    s_vram   = (uint32_t*)info.addr;
    s_pitch  = info.pitch / 4;   // bytes→pixels
    s_width  = info.width;
    s_height = info.height;

    // Allocate back-buffer from heap (width × height × 4 bytes)
    uint32_t sz = s_width * s_height * 4;
    s_back = (uint32_t*)kmalloc(sz);
    if (!s_back) return false;
    k_memset(s_back, 0, sz);
    s_ready = true;
    return true;
}

bool      fb_available()   { return s_ready; }
uint32_t* fb_backbuffer()  { return s_back; }
uint32_t  fb_pitch_px()    { return s_width; }

//  Swap / blit
void fb_swap() {
    if (!s_ready) return;
    for (uint32_t y = 0; y < s_height; y++) {
        uint32_t* src = s_back  + y * s_width;
        uint32_t* dst = s_vram  + y * s_pitch;
        k_memcpy(dst, src, s_width * 4);
    }
}

void fb_swap_rect(int x, int y, int w, int h) {
    if (!s_ready) return;
    if (x < 0) { w += x; x = 0; }
    if (y < 0) { h += y; y = 0; }
    if (x + w > (int)s_width)  w = s_width  - x;
    if (y + h > (int)s_height) h = s_height - y;
    if (w <= 0 || h <= 0) return;
    for (int row = y; row < y + h; row++) {
        uint32_t* src = s_back + row * s_width + x;
        uint32_t* dst = s_vram + row * s_pitch + x;
        k_memcpy(dst, src, (uint32_t)w * 4);
    }
}

//  Pixel access
void fb_put(int x, int y, uint32_t color) {
    s_back[y * (int)s_width + x] = color;
}

void fb_set(int x, int y, uint32_t color) {
    if (!s_ready) return;
    if ((uint32_t)x >= s_width || (uint32_t)y >= s_height) return;
    fb_put(x, y, color);
}

uint32_t fb_get(int x, int y) {
    if (!s_ready) return 0;
    if ((uint32_t)x >= s_width || (uint32_t)y >= s_height) return 0;
    return s_back[y * s_width + x];
}

//  Primitives
void fb_clear(uint32_t color) {
    uint32_t n = s_width * s_height;
    for (uint32_t i = 0; i < n; i++) s_back[i] = color;
}

void fb_fill_rect(int x, int y, int w, int h, uint32_t color) {
    if (!s_ready) return;
    int x1 = x, y1 = y, x2 = x+w, y2 = y+h;
    if (x1 < 0) x1 = 0;
    if (y1 < 0) y1 = 0;
    if (x2 > (int)s_width)  x2 = s_width;
    if (y2 > (int)s_height) y2 = s_height;
    for (int row = y1; row < y2; row++) {
        uint32_t* p = s_back + row * s_width + x1;
        for (int col = x1; col < x2; col++) *p++ = color;
    }
}

void fb_draw_rect(int x, int y, int w, int h, uint32_t color) {
    fb_fill_rect(x,   y,   w, 1, color);
    fb_fill_rect(x,   y+h-1, w, 1, color);
    fb_fill_rect(x,   y,   1, h, color);
    fb_fill_rect(x+w-1, y, 1, h, color);
}

void fb_draw_rect_thick(int x, int y, int w, int h, uint32_t color, int t) {
    fb_fill_rect(x, y, w, t, color);
    fb_fill_rect(x, y+h-t, w, t, color);
    fb_fill_rect(x, y, t, h, color);
    fb_fill_rect(x+w-t, y, t, h, color);
}

void fb_draw_line(int x0, int y0, int x1, int y1, uint32_t color) {
    int dx = x1 - x0; if (dx < 0) dx = -dx;
    int dy = y1 - y0; if (dy < 0) dy = -dy;
    int sx = (x1 > x0) ? 1 : -1;
    int sy = (y1 > y0) ? 1 : -1;
    int err = dx - dy, e2;
    for (;;) {
        fb_set(x0, y0, color);
        if (x0 == x1 && y0 == y1) break;
        e2 = 2 * err;
        if (e2 > -dy) { err -= dy; x0 += sx; }
        if (e2 <  dx) { err += dx; y0 += sy; }
    }
}

void fb_draw_circle(int cx, int cy, int r, uint32_t color) {
    int x = r, y = 0, err = 0;
    while (x >= y) {
        fb_set(cx+x,cy+y,color); fb_set(cx-x,cy+y,color);
        fb_set(cx+x,cy-y,color); fb_set(cx-x,cy-y,color);
        fb_set(cx+y,cy+x,color); fb_set(cx-y,cy+x,color);
        fb_set(cx+y,cy-x,color); fb_set(cx-y,cy-x,color);
        y++;
        err += 1 + 2*y;
        if (2*(err-x) + 1 > 0) { x--; err += 1 - 2*x; }
    }
}

void fb_fill_circle(int cx, int cy, int r, uint32_t color) {
    for (int dy = -r; dy <= r; dy++)
        for (int dx = -r; dx <= r; dx++)
            if (dx*dx + dy*dy <= r*r)
                fb_set(cx+dx, cy+dy, color);
}

//  Blending (integer approximation: dst = dst*(255-a)/255 + src*a/255)
void fb_fill_rect_blend(int x, int y, int w, int h, uint32_t color, uint8_t alpha) {
    if (alpha == 255) { fb_fill_rect(x, y, w, h, color); return; }
    if (alpha == 0)   return;
    uint8_t sr = (color >> 16) & 0xFF;
    uint8_t sg = (color >>  8) & 0xFF;
    uint8_t sb =  color        & 0xFF;
    uint32_t ia = 255 - alpha;
    int x1 = x < 0 ? 0 : x;
    int y1 = y < 0 ? 0 : y;
    int x2 = (x+w) > (int)s_width  ? (int)s_width  : x+w;
    int y2 = (y+h) > (int)s_height ? (int)s_height : y+h;
    for (int row = y1; row < y2; row++) {
        uint32_t* p = s_back + row * s_width + x1;
        for (int col = x1; col < x2; col++, p++) {
            uint8_t dr = (*p >> 16) & 0xFF;
            uint8_t dg = (*p >>  8) & 0xFF;
            uint8_t db =  *p        & 0xFF;
            uint8_t rr = (uint8_t)((sr * alpha + dr * ia) / 255);
            uint8_t rg = (uint8_t)((sg * alpha + dg * ia) / 255);
            uint8_t rb = (uint8_t)((sb * alpha + db * ia) / 255);
            *p = ((uint32_t)rr << 16) | ((uint32_t)rg << 8) | rb;
        }
    }
}

//  Gradient fill (top-to-bottom)
void fb_fill_gradient(int x, int y, int w, int h,
                      uint32_t top, uint32_t bot) {
    if (h <= 0) return;
    uint8_t tr = (top>>16)&0xFF, tg=(top>>8)&0xFF, tb=top&0xFF;
    uint8_t br = (bot>>16)&0xFF, bg=(bot>>8)&0xFF, bb=bot&0xFF;
    for (int row = 0; row < h; row++) {
        uint8_t r = (uint8_t)(tr + (int)(br-tr)*row/h);
        uint8_t g = (uint8_t)(tg + (int)(bg-tg)*row/h);
        uint8_t b = (uint8_t)(tb + (int)(bb-tb)*row/h);
        uint32_t c = ((uint32_t)r<<16)|((uint32_t)g<<8)|b;
        fb_fill_rect(x, y+row, w, 1, c);
    }
}

//  Text rendering
void fb_draw_char(int x, int y, char c, uint32_t fg, uint32_t bg) {
    if (c < 32 || c > 127) c = '?';
    const uint8_t* glyph = FONT8x8[(uint8_t)c - 32];
    bool transparent = (bg == Color::Transparent);
    for (int row = 0; row < 8; row++) {
        uint8_t bits = glyph[row];
        for (int col = 0; col < 8; col++) {
            if (bits & (1 << col))
                fb_set(x+col, y+row, fg);
            else if (!transparent)
                fb_set(x+col, y+row, bg);
        }
    }
}
void fb_draw_text(int x, int y, const char* s, uint32_t fg, uint32_t bg) {
    while (*s) {
        if (*s == '\n') { y += 8; x = 0; s++; continue; }
        fb_draw_char(x, y, *s, fg, bg);
        x += 8;
        s++;
    }
}

int fb_text_width(const char* s) {
    int n = 0;
    while (*s++) n++;
    return n * 8;
}

//  Scroll region upward (for terminal)
void fb_scroll_up(int x, int y, int w, int h, int pixels, uint32_t bg) {
    if (!s_ready || pixels <= 0) return;
    if (pixels >= h) { fb_fill_rect(x, y, w, h, bg); return; }
    int move = h - pixels;
    for (int row = 0; row < move; row++) {
        uint32_t* src = s_back + (y + row + pixels) * s_width + x;
        uint32_t* dst = s_back + (y + row)          * s_width + x;
        k_memcpy(dst, src, (uint32_t)w * 4);
    }
    fb_fill_rect(x, y + move, w, pixels, bg);
}